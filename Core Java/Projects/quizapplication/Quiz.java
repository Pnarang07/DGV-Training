package quizapplication;

public class Quiz {
	
	String name;
	int score;
	
	
	public Quiz(String name, int score) {
		super();
		this.name = name;
		this.score = score;
	}


	public String getName() {
		return name;
	}


	public int getScore() {
		return score;
	}
	
	
	

}
