package quizapplication;

public class QuizPlayer {
	
	private String name;
	private int score;
	
	
	public QuizPlayer(String name, int score) {
		super();
		this.name = name;
		this.score = score;
	}


	public String getName() {
		return name;
	}


	public int getScore() {
		return score;
	}
	
	
	

}
